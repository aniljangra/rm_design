function create_vip_order_callback() {

  global $woocommerce; 
  global $current_user;
	$user_id = $current_user->ID;	

  $address = array(
      'first_name' => $current_user->user_login,
      'email'      => $current_user->user_email,
  );

	$order = wc_create_order();
	// foreach ($_POST['product_order'] as $productId => $productOrdered) :
		$order->add_product( get_product( $_GET['productid'] ), 1 );
	// endforeach;

	$order->set_address( $address, 'billing' );
	$order->set_address( $address, 'shipping' );

	$order->calculate_totals();

	update_post_meta( $order->id, '_payment_method', 'razorpay' );
	update_post_meta( $order->id, '_payment_method_title', 'razorpay' );

	// Store Order ID in session so it can be re-used after payment failure
	WC()->session->order_awaiting_payment = $order->id;
    
	// Process Payment
	$available_gateways = WC()->payment_gateways->get_available_payment_gateways();
	$result = $available_gateways[ 'razorpay' ]->process_payment( $order->id );
	// Redirect to success/confirmation/payment page
	if ( $result['result'] == 'success' ) {
		$result = apply_filters( 'woocommerce_payment_successful_result', $result, $order->id );
		//wp_redirect( $result['redirect'] );
		echo $result['redirect'];
		
		wp_die();
	}
}

